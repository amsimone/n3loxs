static const double eps = 1.e-12;

static const double MH2 = constants::MH*constants::MH;


// NLO q-qbar regular term
double qqb_regular_nlo(const double X[], const double s, const double muf, LHAPDF::PDF const* const pdf)
{
  double tau;
  double x1, x2;
  double fac;
  double res;
  double muf2;

  tau = MH2/s;
  muf2 = muf*muf;


  x1 = exp((eps+(1.0-2.0*eps)*X[0])*log(tau));
  x2 = tau/x1 + (1.0-tau/x1)*(eps+(1.0-2.0*eps)*X[1]);
  fac = -pow(1.0-2.0*eps,2)*x1*(1.0-tau/x1)*log(tau);

  res = 32*(1-x1)*(1-x1)*(1-x1)/27.0;

  res = fac*res;
  res = res*tau*dlumqqb(x2,tau/x1/x2,muf2,pdf)/x1/x2/x1;

  return res;
}


