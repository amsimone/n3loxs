static const double eps = 1.e-12;


// NLO gq regular term
double gq_regular_nlo(const double X[], const double s, const double muf, LHAPDF::PDF const* const pdf)
{
  double tau;
  double x1, x2;
  double fac;
  double res;
  double muf2;
  double log1;
  const double MH2 = constants::MH*constants::MH;

  tau = MH2/s;
  muf2 = muf*muf;


  x1 = exp((eps+(1.0-2.0*eps)*X[0])*log(tau));
  x2 = tau/x1 + (1.0-tau/x1)*(eps+(1.0-2.0*eps)*X[1]);
  fac = -pow(1.0-2.0*eps,2)*x1*(1.0-tau/x1)*log(tau);

  log1 = log(MH2/muf2);
  res = ((4 - 4*x1 + 2*x1*x1)*log1 +
	 4*(2 - 2*x1 + x1*x1)*(log(1.0-x1)-0.5*log(x1)) -
	 3 + 6*x1 -x1*x1)/3.0;

  res = fac*res;
  res = res*tau*dlumgq(x2,tau/x1/x2,muf2,pdf)/x1/x2/x1;

  return res;
}


